# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.
print('5 AND 6 = ', 5 & 6)
print('5 OR 6 = ', 5 | 6)
print('5 XOR 6 = ', 5 ^ 6)
print('bit shift of 5 by two positions to the right = ', 5 >> 2)
print('bit shift of 5 by two positions to the left = ', 5 << 2)
